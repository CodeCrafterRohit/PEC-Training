console.log("Datatypes in JS")

//? 1. Number
let num = 10;
console.log(num);
console.log(typeof(num)); // number

let num2 = 3.14;
console.log(num2);
console.log(typeof(num2)); // number

//? 2. String
let str = "Welcome to Javascript";
console.log(str);
console.log(typeof(str)); //string

let str2 = 'Hello world';
console.log(str2);
console.log(typeof(str2)); //string

//? 3. Boolean (true-1 or false-0)
let isPendingResult = true;
console.log(isPendingResult);
console.log(typeof(isPendingResult)); //boolean

let isHoliday = false;
console.log(isHoliday);
console.log(typeof(isHoliday)); // boolean

//? 4. Null
let empty = null;
console.log(empty);
console.log(typeof(empty)); //object

//? 5. undefined
let name;
console.log(name); //undefined
console.log(typeof(name));  //undefined