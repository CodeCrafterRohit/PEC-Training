console.log("Difference between var, let and const");

// //! Declaration and Initialization of variable
var num = 10;
console.log(num);

// //! Declaration of an variable
var num;
console.log(num);

// //! Re-declaration of an variable
var num;
console.log(num);

// //! Re-intialization of an variable
var num = 20;
console.log(num);

//? let variable 
//! Declaration of let variable
let num;
console.log(num);

// //! Declaration and intialization of let variable
let num1 = 10;
console.log(num1);

num1 = 20;
console.log(num1);

//? const variable
//! Declaration and intialization of const variable (only)
const num = 10;
console.log(num)

num = 20;
console.log(num);



