console.log("Arithmetic Operators");

//! 1. Addition (+)
let num1 = 10;
let num2 = 20;
let result = num1 + num2;
console.log(result); //30
console.log("Addition = ", num1+num2);

//! 2. Substraction (-)
let num3 = 30;
let num4 = 20;
console.log(num3-num4); //10

//! 3. Multiplication (*)
let num5 = 2;
console.log(num5 * 5); //10

//! 4. Division (/)
let num6 = 10;
let divResult = num6 / 2;
console.log(divResult);

//! 5. Modulus (%)
let num7 = 10;
let modulusResult = num7 % 2;
console.log(modulusResult); // 0